package introduction.day1;

public class Day1_3 {

    // 두 수의 곱
    public static void main(String[] args) {

        int num1 = 3;
        int num2 = 4;

        System.out.println("solution => " + solution(num1, num2));

    }

    public static int solution(int num1, int num2) {
        return num1 * num2;
    }

}
